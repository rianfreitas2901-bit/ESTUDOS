def soma(A, B):
    s = A + B
    return(s)

#-------------------------------------------------------------------
def subtracao(A, B):
    sc = A - B
    return(sc)

#-------------------------------------------------------------------
def multiplicacao(A, B):
    m = A * B
    return(m)

#-------------------------------------------------------------------
def divisao(A, B):
    d = A / B
    return(d)
